def convertir_columnas(df):
    df = df.copy()
    def moneda_a_num(val):
        if pd.isna(val): return np.nan
        s = str(val).replace("\u20ac", "").replace(",", "").strip()
        if "B" in s: return float(s.replace("B", "")) * 1_000_000_000
        if "M" in s: return float(s.replace("M", "")) * 1_000_000
        if "K" in s: return float(s.replace("K", "")) * 1_000
        try: return float(s)
        except: return np.nan
    def altura_a_num(val):
        if pd.isna(val): return np.nan
        s = str(val).replace('"', "").strip()
        m = re.match(r"(\d+)\s*cm", s)
        if m: return float(m.group(1))
        m = re.match(r"(\d+)'\s*(\d+)\"", s)
        if m: return float(m.group(1)) * 30.48 + float(m.group(2)) * 2.54
        try: return float(s)
        except: return np.nan
    def peso_a_num(val):
        if pd.isna(val): return np.nan
        s = str(val).lower().strip()
        m = re.search(r"(\d+\.?\d*)\s*(kg|kgs?)", s)
        if m: return float(m.group(1))
        m = re.search(r"(\d+\.?\d*)\s*(lbs?|pounds?)", s)
        if m: return float(m.group(1)) * 0.453592
        m = re.search(r"(\d+\.?\d*)", s)
        if m: return float(m.group(1))
        return np.nan
    cols_moneda = [c for c in df.columns if c in ["Value", "Wage", "Release Clause"]]
    for c in cols_moneda:
        print(f"  Convirtiendo {c}...")
        df[c] = df[c].apply(moneda_a_num)
    if "Height" in df.columns:
        print("  Convirtiendo Height...")
        df["Height"] = df["Height"].apply(altura_a_num)
    if "Weight" in df.columns:
        print("  Convirtiendo Weight...")
        df["Weight"] = df["Weight"].apply(peso_a_num)
    return df

